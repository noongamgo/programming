'''
    작성일 : 2024년 4월 4일
    작성자 : 학과 학번 이름
    설명 : 점수를 입력받아 학점을 출력 하시오.
        90~100 : A학점
        80~89 : B학점  
        70~79 : C학점
        60~69 : D학점
        0~59 : F학점
    
    [문제분석]
        필요한 변수 : score (점수)
        점수가 90점에서 100점 사이이다.
            => 1) 90 <= score <= 100
            => 2) (score >= 90) and (score <= 100)
        f학점은 나머지 처리(else) 한다.
        점수는 0~100 점 사이다.
    [알고리즘]
        1. 점수를 입력받는다.
        2. 막약 점수가 100점을 넘으면 잘못된점수
        3. 아니고 만약 점수가 90~100이면
            2-1. A학점
        4. 아니고 만약 점수가 80~89 이면
            3-1. B학점
        5. 아니고 만약 점수가 70~79 이면
            4-1. C학점
        6. 아니고 만약 점수가 60~69 이면
            5-1. D학점
        7. 아니면 나머지 처리
        
'''
score = int(input("학점을 입력하시오. : " ))

if score > 100 :
    print("학점을 정확히 입력하시오.")
elif score >= 90 : 
    print(f"{score}은(는) A학점 입니다")
elif score >= 80 :
    print(f"{score}은(는) B학점 입니다")
elif score >= 70 :
    print(f"{score}은(는) C학점 입니다")
elif score >= 60 :
    print(f"{score}은(는) D학점 입니다")
else : 
    print(f"{score}은(는) F학점 입니다")